#ifndef _mpqc_src_lib_math_scmat_predicate_h
#define _mpqc_src_lib_math_scmat_predicate_h

#include <cmath>

namespace sc {

  /// fabs_less(a,b) return true if fabs(a) < fabs(b)
  template <typename T>
  struct fabs_less : std::binary_function<T, T, bool> {
    fabs_less() {}
    bool operator()(T a, T b) { return fabs(a) < fabs(b); }
  };

} // end of namespace sc

#endif // end of header guard


