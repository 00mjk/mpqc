/* macro to receive a message */
/* arguments should be a pointer and 3 integers, respectively */

#define ENVRECV(buf, bytes, type, checking) \
  if ( checking == 1 ) envRECV( buf , bytes , type ); \
    else _crecv( type , buf , bytes )


