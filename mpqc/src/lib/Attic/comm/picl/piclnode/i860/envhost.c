#include "globals.h"
#include "envglobals.h"

/* envhost determines whether the node process was loaded by a host */
/* process (1) or not (0) */

int envhost()
{
  char *getenv();

  if (getenv("HOSTED") != NULL) envHOSTED = 1; 
    else envHOSTED = 0;

}





